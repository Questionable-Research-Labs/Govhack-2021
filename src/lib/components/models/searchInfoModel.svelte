    <h3>Filter by Address/Location</h3>
    <p>
        The search box preforms a <a href="https://en.wikipedia.org/wiki/Approximate_string_matching" referrerpolicy="no-referrer" target="_blank">fuzzy search</a> on the titles and addresses of the Locations of Interest as you type.
        This means the search will prefer false positives than false negatives, and disregards the order of letters that you type.
